#-------- 問4 ① ---------#
mu <- 23.7             # 平均
std_dev <- 3.6         # 標準偏差
# P(18 < X < 25) の計算
p <- pnorm(25, mean = mu, sd = std_dev) - pnorm(18, mean = mu, sd = std_dev)
# 結果を表示(教科書は標準正規分布表に基づく計算のため誤差がある)
cat("P(18 < X < 25) ", p, "\n")

#-------- 問4 ② ---------#
# 上位10%に該当するZ値（パーセンタイル）
z_10 <- qnorm(0.9)

# BMIの閾値を計算
threshold_bmi <- mu + z_10 * std_dev
# 結果の表示
cat(sprintf("BMI %.1f\n", round(threshold_bmi, 1)))


