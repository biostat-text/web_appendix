#-------- 問2 ① ---------#
n <- 20        # 試行回数（患者数）
p <- 0.41      # 奏効率（成功確率）

# 期待値と分散
expected_value <- n * p
variance <- n * p * (1 - p)
# 結果の表示
cat("期待値", expected_value, "\n")
cat("分散", variance, "\n")

#-------- 問2 ② ---------#
n <- 20        # 試行回数（患者数）
p <- 0.41      # 奏効率（成功確率）

# P(X ≥ 15) = 1 - P(X ≤ 14)を計算
prob_15_or_more <- 1 - pbinom(14, size = n, prob = p)
# 結果の表示
cat("P(X ≥ 15)", prob_15_or_more, "\n")
cat(sprintf("P(X ≥ 15) %.3f\n", round(prob_15_or_more, 3)))


