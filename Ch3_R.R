# ----- 3-2-2 二項分布 -----#
library(MASS)    # fractions関数使用のため

# パラメータ設定
n <- 3               # 抽出人数
p <- 1/4             # 喫煙率

# 喫煙者数の出現確率を計算
x <- 0:n             # x=0, 1, 2, 3
prob <- dbinom(x, size = n, prob = p)

# 分数に変換
frac_prob <- as.character(fractions(prob))

# 表3-3の表示
table3_3 <- rbind(
  X    = c("", x),
  確率 = c("", frac_prob)
)
noquote(table3_3)     # 見やすく表示（"" の引用符を消す）

# 二項分布の期待値と分散
e <- n * p            # 期待値
v <- n * p * (1 - p)  # 分散

# 分数に変換
frac_e <- as.character(fractions(e))
frac_v <- as.character(fractions(v))

# 結果の表示
cat("期待値", frac_e, "\n")
cat("分散", frac_v, "\n")

# ----- 3-2-3 ポアソン分布 -----#
# データの読み込み
data(discoveries)

# 平均 λ 
lambda <- mean(discoveries)
cat("平均 λ ", lambda, "\n")

# 平均発明件数λにおける発明件数0〜6件の確率を出力
d <- 0:6                         # 発明件数
probs <- dpois(d, lambda)        # 発明件数0〜6件の確率
# 結果の表示
names(probs) <- paste0(d, "件")
print(probs)

# P(X ≥ 9) を計算：P(X ≥ 9) = 1 - P(X ≤ 8)
p_ge_9 <- 1 - ppois(8, lambda)
# 結果の表示
cat("P(X ≥ 9) =", p_ge_9, "\n")


