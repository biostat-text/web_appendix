# ----- 3-3-3 標準正規分布 -----#
# P(0 ≤ Z ≤ 0.5)
p1 <- pnorm(0.5) - pnorm(0)
# 結果を表示
cat("P(0 ≤ Z ≤ 0.5) =", p1, "\n")

#	P(-0.8 ≤ Z ≤ 0)
p2 <- pnorm(0) - pnorm(-0.8)
# 結果を表示
cat("P(-0.8 ≤ Z ≤ 0) =", p2, "\n")

# P(-0.8 ≤ Z ≤ 0.5)
p3 <- pnorm(0.5) - pnorm(-0.8)
# 結果を表示
cat("P(-0.8 ≤ Z ≤ 0.5) =", p3, "\n")

# P(Z ≤ -0.8)
p4 <- pnorm(-0.8)
# 結果を表示
cat("P(Z ≤ -0.8) =", p4, "\n")

#	P(Z ≥ -0.8)
p5 <- 1 - pnorm(-0.8)
# 結果を表示
cat("P(Z ≥ -0.8) =", p5, "\n")

